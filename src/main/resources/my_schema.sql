CREATE TABLE CLIENTE (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(100)
    CPF VARCHAR(11)
);

CREATE TABLE CONTA (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    NUMERO_CONTA NUMERIC(20,2),
    TITULAR_CONTA VARCHAR(100),
    SALDO NUMERIC(20,2)
);

CREATE TABLE CLIENTE_CONTA(
     ID INTEGER PRIMARY KEY AUTO_INCREMENT,
     CONTA_CLIENTE INTEGER REFERENCES CLIENTE (ID),
     CLIENTE_CONTA INTEGER REFERENCES CONTA (ID)
);